#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *dest_temp;

	dest_temp = s1;
	while (*dest_temp != '\0')
		dest_temp++;
	ft_strcpy(dest_temp, s2);
	return (s1);
}
